#entrada nota 0 a 100
#nota maior ou igual a 90 #saida A
#nota maior ou igual a 80 #saida B
#nota maior ou igual a 70 #saida C
#nota menor que 70 #saida D

nota=float(input('digite sua nota: '))
if nota>=90:
    print('Nota A')
elif nota>=80:
    print('Nota B')
elif nota>=70:
    print('Nota C')
else:
    print('Nota C')